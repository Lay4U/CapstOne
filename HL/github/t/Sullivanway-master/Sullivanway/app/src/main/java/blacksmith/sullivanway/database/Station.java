    public static String getNextStnNm(String lineNm, String stnNm, int toward) {
        Cursor cursor = db.rawQuery(String.format("SELECT id FROM %s WHERE lineNm='%s' AND stnNm='%s'", TB_NAME, lineNm, stnNm), null);
        int id = -1;
        if (cursor.moveToNext())
            id = cursor.getInt(0);
        cursor.close();
        String mStnNm = null;
        if (id != -1) {
            id = (toward == UPWARD) ? id - 1 : id + 1;
            cursor = db.rawQuery(String.format(MainActivity.DEFAULT_LOCALE, "SELECT stnNm FROM %s WHERE lineNm='%s' AND id=%d", TB_NAME, lineNm, id), null);
            if (cursor.moveToNext())
                mStnNm = cursor.getString(0);
            cursor.close();
        }
        return mStnNm;
    }

    public static int getToward(Station startStn, Station endStn) {
        int startId = -1, endId = -1;
        Cursor cursor = db.rawQuery(String.format("SELECT id FROM %s WHERE lineNm='%s' AND stnNm='%s'", TB_NAME, startStn.getLineNm(), startStn.getStnNm()), null);
        if (cursor.moveToNext())
            startId = cursor.getInt(0);
        cursor.close();

        cursor = db.rawQuery(String.format("SELECT id FROM %s WHERE lineNm='%s' AND stnNm='%s'", TB_NAME, endStn.getLineNm(), endStn.getStnNm()), null);
        if (cursor.moveToNext())
            endId = cursor.getInt(0);
        cursor.close();

        return (startId > endId) ? UPWARD : DOWNWARD;
    }

    public static int compare(Station stnA, Station stnB) {
        if (stnA.getStnNm().equals(stnB.getStnNm())) {
            if (stnA.getLineNm().equals(stnB.getLineNm())) {
                return 0; //같은역
            } else {
                return 1; //환승역
            }
        } else {
            return 2; //다른역
        }

    }

    public static ArrayList<String> getLines(String lineNm, String stnNm) {
        ArrayList<String> lines = new ArrayList<>();
        Cursor cursor = db.rawQuery(String.format("SELECT pointx, pointy FROM %s WHERE lineNm='%s' AND stnNm='%s'", TB_NAME, lineNm, stnNm), null);
        if (cursor.moveToNext()) {
            int pointx = cursor.getInt(0);
            int pointy = cursor.getInt(1);

            cursor.close();
            cursor = db.rawQuery(String.format(MainActivity.DEFAULT_LOCALE, "SELECT lineNm FROM %s WHERE pointx=%d AND pointy=%d", TB_NAME, pointx, pointy), null);
            while (cursor.moveToNext())
                lines.add(cursor.getString(0));
        }
        cursor.close();
        return lines;
    }

